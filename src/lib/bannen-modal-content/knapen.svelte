<h3>Woordje van de leiding</h3>
<div class="box">
	<p>
    De knapenban staat garant voor spel en plezier, en bezorgt 12 tot 14-jarigen hun tweewekelijks zondags vertier. 
		In de zomer gaan we op tentenkamp, vergeet zeker niet je koplamp.
		We leren sjorren en maken vuur, bij de knapenban beleef je altijd de leukste 3 uur.    
	</p>
